package com.effort.entity;



public class EmployeeCustomEntitySpecSettings {

	private long id;
	private long companyId;
	private long empId;
	private long customEntitySpecId;
	private String key;
	private String value;
	private String modifiedDate;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getCompanyId() {
		return companyId;
	}
	public void setCompanyId(long companyId) {
		this.companyId = companyId;
	}
	public long getEmpId() {
		return empId;
	}
	public void setEmpId(long empId) {
		this.empId = empId;
	}
	public long getCustomEntitySpecId() {
		return customEntitySpecId;
	}
	public void setCustomEntitySpecId(long customEntitySpecId) {
		this.customEntitySpecId = customEntitySpecId;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getModifiedDate() {
		return modifiedDate;
	}
	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}
	
	
}
